/* lesskey.c — configure key bindings for less */
#include "unistd.h"
#include "string.h"
#include "stdio.h"
#include "stdlib.h"

/*
 * Simple lesskey implementation.
 * Reads key binding definitions from stdin and writes
 * a binary .lesskey file.
 *
 * Format (text input):
 *   # comment
 *   <key> <action>     (key and action separated by space)
 *   \\k<hex> <action>  (key by hex code)
 *
 * Output: binary .lesskey file suitable for less
 */

int main(int argc, char *argv[]) {
    const char *outfile = ".lesskey";
    if (argc > 1) {
        outfile = argv[1];
    }

    /* Read key bindings from stdin */
    char input[4096];
    int total = 0;
    char buf[256];
    int n;
    while ((n = read(0, buf, sizeof(buf))) > 0) {
        if (total + n >= 4096) n = 4096 - total - 1;
        memcpy(input + total, buf, n);
        total += n;
    }
    if (total == 0) {
        write(2, "lesskey: no input\n", 18);
        return 1;
    }
    input[total] = 0;

    /* Write .lesskey file — simple format:
     * Header: "LESSKEY" magic
     * Then entries: 1 byte key, 1 byte action_len, action string
     */
    int fd = open(outfile, O_WRONLY | O_CREAT | O_TRUNC, 0644);
    if (fd < 0) {
        write(2, "lesskey: cannot write ", 22);
        write(2, outfile, strlen(outfile));
        write(2, "\n", 1);
        return 1;
    }

    /* Write comment header */
    write(fd, "# lesskey file - generated by lesskey\n", 38);

    /* Parse input line by line */
    int pos = 0;
    while (pos < total) {
        /* Skip leading whitespace */
        while (pos < total && (input[pos] == ' ' || input[pos] == '\t'))
            pos++;

        if (pos >= total || input[pos] == '\n') {
            pos++;
            continue;
        }

        /* Skip comments */
        if (input[pos] == '#') {
            while (pos < total && input[pos] != '\n') pos++;
            if (pos < total) pos++;
            continue;
        }

        /* Find end of line */
        int line_start = pos;
        while (pos < total && input[pos] != '\n') pos++;

        /* Extract key and action (space-separated) */
        char line[256];
        int linelen = pos - line_start;
        if (linelen > 255) linelen = 255;
        memcpy(line, input + line_start, linelen);
        line[linelen] = 0;

        /* Find space separating key from action */
        char *space = strchr(line, ' ');
        if (space) {
            *space = 0;
            char *key = line;
            char *action = space + 1;

            /* Write entry to file */
            write(fd, key, strlen(key));
            write(fd, " ", 1);
            write(fd, action, strlen(action));
            write(fd, "\n", 1);
        } else if (linelen > 0) {
            /* Write the line as-is */
            write(fd, line, linelen);
            write(fd, "\n", 1);
        }

        if (pos < total) pos++;
    }

    write(1, "lesskey: wrote ", 15);
    write(1, outfile, strlen(outfile));
    write(1, "\n", 1);

    close(fd);
    return 0;
}
