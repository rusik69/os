size_t base64_encode(char *out, const uint8_t *in, size_t in_len)
{
    size_t i, out_len = 0;

    for (i = 0; i < in_len; i += 3) {
        unsigned int b = (unsigned int)in[i] << 16;
        if (i + 1 < in_len)
            b |= (unsigned int)in[i + 1] << 8;
        if (i + 2 < in_len)
            b |= in[i + 2];

        out[out_len++] = base64_table[(b >> 18) & 0x3F];
        out[out_len++] = base64_table[(b >> 12) & 0x3F];

        if (i + 1 < in_len)
            out[out_len++] = base64_table[(b >> 6) & 0x3F];
        else
            out[out_len++] = '=';

        if (i + 2 < in_len)
            out[out_len++] = base64_table[b & 0x3F];
        else
            out[out_len++] = '=';
    }
    out[out_len] = '\0';
    return out_len;
}

size_t base64_decode(uint8_t *out, const char *in, size_t in_len)
{
    size_t i, out_len = 0;

    if (in_len & 3)
        return (size_t)-1;

    for (i = 0; i < in_len; i += 4) {
        uint8_t a[4];
        unsigned int b;

        for (int j = 0; j < 4; j++) {
            uint8_t v = reverse_table[(uint8_t)in[i + j]];
            if (v == 0xFF && in[i + j] != '=')
                return (size_t)-1;
            a[j] = v;
        }

        b = ((unsigned int)a[0] << 18) |
            ((unsigned int)a[1] << 12) |
            ((unsigned int)a[2] << 6) |
            a[3];

        out[out_len++] = (uint8_t)(b >> 16);
        if (a[2] != 0xFF)
            out[out_len++] = (uint8_t)(b >> 8);
        if (a[3] != 0xFF)
            out[out_len++] = (uint8_t)b;
    }
    return out_len;
}

