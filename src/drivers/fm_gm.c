/*
 * fm_gm.c — General MIDI Level 1 instrument definitions for FM synthesis
 *
 * Defines 128 GM program-to-FM-parameter mappings for the 2-operator
 * FM synthesis engine.  Each instrument specifies carrier/modulator
 * waveforms, frequency ratios, output levels, modulation index, and
 * ADSR envelope parameters.
 *
 * The table is indexed by GM program number (0-127).  Default program
 * for every MIDI channel is 0 (Acoustic Grand Piano).
 *
 * Task #12 — D142: MIDI GM instrument mapping
 */

#include "fm_synth.h"

/*
 * Macro for building a compact GM instrument definition.
 *   wc, wm  = carrier / modulator waveform (fm_waveform enum)
 *   cr, mr  = freq multiplier x2 (2=1.0x, 4=2.0x, 1=0.5x, 3=1.5x...)
 *   cl, ml  = carrier / modulator output level (0-255)
 *   mi      = modulation index (0-255)
 *   ca, cd, cs, crr = carrier attack, decay, sustain, release
 *   ma, md, ms, mrr = modulator attack, decay, sustain, release
 */
#define GM(wc,wm,cr,mr,cl,ml,mi, ca,cd,cs,crr, ma,md,ms,mrr) \
	{(uint8_t)(wc),(uint8_t)(wm),(uint8_t)(cr),(uint8_t)(mr), \
	 (uint8_t)(cl),(uint8_t)(ml),(uint8_t)(mi), \
	 (uint8_t)(ca),(uint8_t)(cd),(uint8_t)(cs),(uint8_t)(crr), \
	 (uint8_t)(ma),(uint8_t)(md),(uint8_t)(ms),(uint8_t)(mrr)}

