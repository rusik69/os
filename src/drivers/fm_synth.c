/*
 * fm_synth.c — FM synthesis engine for MIDI playback
 *
 * Implements a software 2-operator FM synthesiser modelled after the
 * classic Yamaha OPL/OPL2 architecture.  Each voice is a carrier +
 * modulator pair where the modulator's output frequency-modulates the
 * carrier's phase to produce harmonically rich timbres.
 *
 * Key design:
 *   - 1024-entry sine lookup table (int16_t, pre-computed)
 *   - 32-bit phase accumulators (10-bit sine index + 22-bit fraction)
 *   - ADSR envelope per operator (attack/decay/sustain/release)
 *   - Polyphony: up to FM_SYNTH_MAX_VOICES simultaneous voices
 *   - Output: 16-bit signed PCM (mono or stereo interleaved)
 *   - All integer arithmetic (no floating point in hot path)
 *
 * The module registers itself with the MIDI sequencer so that
 * midi_seq_note_on/off events are automatically routed through
 * the FM synthesiser instead of the PC speaker.
 *
 * Task #11 — D142: MIDI synthesiser (FM synthesis baseline)
 */

