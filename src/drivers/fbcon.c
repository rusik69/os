static uint32_t  g_height = 0;
static uint32_t  g_pitch = 0;     /* bytes per line */
static int       g_cursor_x = 0;
static int       g_cursor_y = 0;
static uint8_t   g_fg = FBCON_LIGHT_GREY;
static uint8_t   g_bg = FBCON_BLACK;

/* Character buffer (80x25) */
static struct fbcon_cell g_buffer[FBCON_ROWS][FBCON_COLS];

/* Scrollback buffer (ring buffer) */
#define SCROLLBACK_SIZE FBCON_SCROLLBACK_LINES
static struct fbcon_cell g_scrollback[SCROLLBACK_SIZE][FBCON_COLS];
static int g_scrollback_head = 0;   /* next write position */
static int g_scrollback_count = 0;  /* total stored lines */
static int g_scrollback_view = 0;   /* >0 = viewing scrollback, 0 = live */

/* ── Internal helpers ─────────────────────────────────────────────── */

/* Pack an RGB colour into 32-bit BGRX framebuffer format */
static inline uint32_t fb_make_colour(uint8_t r, uint8_t g, uint8_t b) {
    return ((uint32_t)b << 0) | ((uint32_t)g << 8) | ((uint32_t)r << 16);
}

/* VGA palette to RGB approximation */
static uint32_t fb_palette(uint8_t index) {
    /* Standard VGA 16-colour palette approximated */
    static const uint32_t pal[16] = {
        0x000000, /* black  */
        0x0000AA, /* blue   */
        0x00AA00, /* green  */
        0x00AAAA, /* cyan   */
        0xAA0000, /* red    */
        0xAA00AA, /* magenta*/
        0xAA5500, /* brown  */
        0xAAAAAA, /* ltgrey */
        0x555555, /* dkgrey */
        0x5555FF, /* ltblue */
        0x55FF55, /* ltgreen*/
        0x55FFFF, /* ltcyan */
        0xFF5555, /* ltred  */
        0xFF55FF, /* ltmgn  */
        0xFFFF55, /* yellow */
        0xFFFFFF, /* white  */
    };
    if (index < 16) return pal[index];
    return pal[7];
}

/* Draw a single character glyph at pixel (px, py) */
static void draw_glyph(int px, int py, uint8_t ch, uint32_t fg_col, uint32_t bg_col) {
    if (ch >= 128) ch = 0x20; /* non-printable */
    const uint8_t *glyph = font8x16[ch];

    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = glyph[row];
        uint32_t *line = &g_fb[(py + row) * (g_pitch / 4) + px];
        for (int col = 0; col < FONT_WIDTH; col++) {
            line[col] = (bits & (0x80 >> col)) ? fg_col : bg_col;
        }
    }
}

/* Redraw one line of the console (from buffer or scrollback) */
static void __attribute__((unused)) redraw_line(int screen_row, int buffer_row) {
    int py = screen_row * FONT_HEIGHT;
    uint32_t fg_c = fb_palette(g_fg);
    uint32_t bg_c = fb_palette(g_bg);
    (void)fg_c; (void)bg_c;

    for (int col = 0; col < FBCON_COLS; col++) {
        struct fbcon_cell *cell;
        if (buffer_row >= 0 && buffer_row < FBCON_ROWS) {
            cell = &g_buffer[buffer_row][col];
        } else {
            /* Use default colours for blank line */
            static struct fbcon_cell blank = {0x20, FBCON_LIGHT_GREY, FBCON_BLACK};
            cell = &blank;
        }
        uint32_t fg = fb_palette(cell->fg);
        uint32_t bg = fb_palette(cell->bg);
        draw_glyph(col * FONT_WIDTH, py, cell->ch ? cell->ch : 0x20, fg, bg);
    }
}

/* Scroll the display forward by one line */
static void scroll_up(void) {
    /* Save top line to scrollback buffer */
    int sb_idx = g_scrollback_head;
    for (int col = 0; col < FBCON_COLS; col++) {
        g_scrollback[sb_idx][col] = g_buffer[0][col];
    }
    g_scrollback_head = (g_scrollback_head + 1) % SCROLLBACK_SIZE;
    if (g_scrollback_count < SCROLLBACK_SIZE) g_scrollback_count++;

    /* Shift buffer up */
    for (int row = 0; row < FBCON_ROWS - 1; row++) {
        for (int col = 0; col < FBCON_COLS; col++) {
            g_buffer[row][col] = g_buffer[row + 1][col];
        }
    }

    /* Clear bottom line */
    for (int col = 0; col < FBCON_COLS; col++) {
        g_buffer[FBCON_ROWS - 1][col].ch = 0x20;
        g_buffer[FBCON_ROWS - 1][col].fg = g_fg;
        g_buffer[FBCON_ROWS - 1][col].bg = g_bg;
    }
}

/* ── Public API ───────────────────────────────────────────────────── */

void fbcon_init(uint32_t *fb, uint32_t w, uint32_t h, uint32_t pitch) {
    g_fb = fb;
    g_width = w;
    g_height = h;
    g_pitch = pitch;

    /* Clear the entire framebuffer */
    uint32_t bg = fb_palette(FBCON_BLACK);
    for (uint32_t y = 0; y < h; y++) {
        for (uint32_t x = 0; x < w; x++) {
            g_fb[y * (pitch / 4) + x] = bg;
        }
    }

    /* Initialise character buffer with spaces */
    for (int r = 0; r < FBCON_ROWS; r++) {
        for (int c = 0; c < FBCON_COLS; c++) {
            g_buffer[r][c].ch = 0x20;
            g_buffer[r][c].fg = g_fg;
            g_buffer[r][c].bg = g_bg;
        }
    }

    g_cursor_x = 0;
    g_cursor_y = 0;
    g_scrollback_head = 0;
    g_scrollback_count = 0;
    g_scrollback_view = 0;
}

void fbcon_putchar(char c) {
    if (!g_fb) return;

    /* Reset scrollback view on new output */
    g_scrollback_view = 0;

    switch (c) {
    case '\n':
        g_cursor_x = 0;
        g_cursor_y++;
        if (g_cursor_y >= FBCON_ROWS) {
            scroll_up();
            g_cursor_y = FBCON_ROWS - 1;
        }
        break;
    case '\r':
        g_cursor_x = 0;
        break;
    case '\b':
        if (g_cursor_x > 0) g_cursor_x--;
        break;
    case '\t':
        do {
            g_cursor_x++;
        } while (g_cursor_x < FBCON_COLS && g_cursor_x % 8 != 0);
        break;
    case '\a': /* BEL — console bell */
        speaker_bell();
        break;
    default:
        if (c < 0x20) break; /* non-printable */
        g_buffer[g_cursor_y][g_cursor_x].ch = (uint8_t)c;
        g_buffer[g_cursor_y][g_cursor_x].fg = g_fg;
        g_buffer[g_cursor_y][g_cursor_x].bg = g_bg;
        g_cursor_x++;
        if (g_cursor_x >= FBCON_COLS) {
            g_cursor_x = 0;
            g_cursor_y++;
            if (g_cursor_y >= FBCON_ROWS) {
                scroll_up();
                g_cursor_y = FBCON_ROWS - 1;
            }
        }
        break;
    }

    /* Draw the glyph at the cursor position (or redraw entire visible area) */
    fbcon_redraw();
}

void fbcon_write(const char *s) {
    while (s && *s) {
        fbcon_putchar(*s++);
    }
}

void fbcon_clear(void) {
    for (int r = 0; r < FBCON_ROWS; r++) {
        for (int c = 0; c < FBCON_COLS; c++) {
            g_buffer[r][c].ch = 0x20;
            g_buffer[r][c].fg = g_fg;
            g_buffer[r][c].bg = g_bg;
        }
    }
    g_cursor_x = 0;
    g_cursor_y = 0;
    fbcon_redraw();
}

void fbcon_set_fg(uint8_t fg) { g_fg = fg; }
void fbcon_set_bg(uint8_t bg) { g_bg = bg; }

void fbcon_scroll(int lines) {
    if (lines > 0) {
        /* Scroll forward (toward live) */
        g_scrollback_view -= lines;
        if (g_scrollback_view < 0) g_scrollback_view = 0;
    } else if (lines < 0) {
        /* Scroll backward (into scrollback) */
        int max_view = (g_scrollback_count < FBCON_ROWS) ? 0
                      : g_scrollback_count - FBCON_ROWS;
        g_scrollback_view -= lines;  /* lines is negative, so this adds */
        if (g_scrollback_view > max_view) g_scrollback_view = max_view;
    }
    fbcon_redraw();
}

void fbcon_scroll_home(void) {
    g_scrollback_view = 0;
    fbcon_redraw();
}

void fbcon_set_cursor(int col, int row) {
    if (col < 0) col = 0;
    if (col >= FBCON_COLS) col = FBCON_COLS - 1;
    if (row < 0) row = 0;
    if (row >= FBCON_ROWS) row = FBCON_ROWS - 1;
    g_cursor_x = col;
    g_cursor_y = row;
}

void fbcon_redraw(void) {
    if (!g_fb) return;

    /* If viewing scrollback, render from the scrollback buffer */
    if (g_scrollback_view > 0 && g_scrollback_count > 0) {
        int sb_start = (g_scrollback_head - g_scrollback_count + SCROLLBACK_SIZE)
                       % SCROLLBACK_SIZE;
        int base = g_scrollback_count - FBCON_ROWS - g_scrollback_view;
        if (base < 0) base = 0;
        int sb_idx = (sb_start + base) % SCROLLBACK_SIZE;

        for (int row = 0; row < FBCON_ROWS; row++) {
            int py = row * FONT_HEIGHT;
            for (int col = 0; col < FBCON_COLS; col++) {
                struct fbcon_cell *cell = &g_scrollback[sb_idx][col];
                uint32_t fg = fb_palette(cell->fg);
                uint32_t bg = fb_palette(cell->bg);
                draw_glyph(col * FONT_WIDTH, py, cell->ch ? cell->ch : 0x20, fg, bg);
            }
            sb_idx = (sb_idx + 1) % SCROLLBACK_SIZE;
        }
        return;
    }

    /* Normal live rendering */
    for (int row = 0; row < FBCON_ROWS; row++) {
        int py = row * FONT_HEIGHT;
        for (int col = 0; col < FBCON_COLS; col++) {
            struct fbcon_cell *cell = &g_buffer[row][col];
            uint32_t fg = fb_palette(cell->fg);
            uint32_t bg = fb_palette(cell->bg);

            /* Draw cursor at current position */
            if (row == g_cursor_y && col == g_cursor_x) {
                uint32_t tmp = fg;
                fg = bg;
                bg = tmp;
            }

            draw_glyph(col * FONT_WIDTH, py, cell->ch ? cell->ch : 0x20, fg, bg);
        }
    }
}

